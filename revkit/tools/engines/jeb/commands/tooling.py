_RUNNER_TEMPLATE = '''\
import com.pnfsoftware.jeb.{cls};
import java.util.Arrays;

/**
 * JEB {ver} Headless Script Runner - bypasses Launcher.main() and invokes {cls} directly.
 * Auto-generated by: jeb-cli gen-runner
 * Usage: java [JVM opts] -cp "bin/app/*;." JebScriptRunner <script_path> [-- extra_args...]
 */
public class JebScriptRunner {{
    public static void main(String[] args) throws Exception {{
        if (args.length < 1) {{
            System.err.println("Usage: JebScriptRunner <script_path> [-- extra_args...]");
            System.exit(1);
        }}

        String scriptPath = args[0];
        String libDir = null;

        // collect extra args after "--"
        String[] extraArgs = new String[0];
        for (int i = 1; i < args.length; i++) {{
            if ("--".equals(args[i])) {{
                if (i + 1 < args.length) {{
                    extraArgs = Arrays.copyOfRange(args, i + 1, args.length);
                }}
                break;
            }}
        }}

        {cls} runner = new {cls}(false, scriptPath, libDir, extraArgs);
        runner.initialize(new String[]{{}});
        runner.start();
        runner.stop();
    }}
}}
'''


def _find_runner_class(jeb_dir, config):
    """Scan jeb.jar for the HeadlessClientContext script runner class.

    Returns (class_short_name, jeb_version_string) or (None, None).
    """
    import subprocess as sp
    import re

    jar_path = os.path.join(jeb_dir, "bin", "app", "jeb.jar")
    if not os.path.isfile(jar_path):
        _log_err(f"jeb.jar not found: {jar_path}")
        return None, None

    _, _, jar_bin, javap_bin = _java_tools(config)

    # Step 1: list classes in com/pnfsoftware/jeb/ (top-level only, short names)
    _log_info("Scanning jeb.jar for script runner class...")
    try:
        out = sp.check_output([jar_bin, "tf", jar_path],
                              stderr=sp.DEVNULL, text=True, timeout=30)
    except FileNotFoundError:
        _log_err(f"jar not found: {jar_bin}")
        _log_info("Set jeb.java_home in config.json or add Java JDK to PATH")
        return None, None
    except Exception as e:
        _log_err(f"jar tf failed: {e}")
        return None, None

    # Candidates: com/pnfsoftware/jeb/XX.class where XX is 1-3 chars (obfuscated)
    candidates = []
    for line in out.splitlines():
        line = line.strip()
        if not line.startswith("com/pnfsoftware/jeb/"):
            continue
        if line.count("/") != 3:  # exactly com/pnfsoftware/jeb/XX.class
            continue
        if not line.endswith(".class"):
            continue
        name = line.rsplit("/", 1)[1].replace(".class", "")
        if len(name) <= 3 and not name.startswith("$"):
            candidates.append(name)

    if not candidates:
        _log_err("No short-named candidate classes found in com.pnfsoftware.jeb")
        return None, None

    _log_info(f"Found {len(candidates)} candidates: {', '.join(sorted(candidates))}")

    # Step 2: javap each candidate to find HeadlessClientContext subclass
    cp = jar_path
    found = None
    for name in sorted(candidates):
        fqn = f"com.pnfsoftware.jeb.{name}"
        try:
            jout = sp.check_output(
                [javap_bin, "-cp", cp, "-public", fqn],
                stderr=sp.DEVNULL, text=True, timeout=15)
        except Exception:
            continue

        # Check: extends HeadlessClientContext AND has (boolean, String, String, String[])
        if "HeadlessClientContext" not in jout:
            continue
        if re.search(r"(boolean,\s*java\.lang\.String,\s*java\.lang\.String,\s*java\.lang\.String\[\])", jout):
            found = name
            _log_ok(f"Found script runner class: {fqn}")
            break

    if not found:
        _log_err("Could not find HeadlessClientContext subclass with expected constructor")
        _log_info("Known classes: JM (5.38), bC (5.37), Om (5.36)")
        return None, None

    # Step 3: try to get JEB version from Launcher
    version = "unknown"
    try:
        lout = sp.check_output(
            [javap_bin, "-cp", cp, "-constants", "com.pnfsoftware.jeb.Launcher"],
            stderr=sp.DEVNULL, text=True, timeout=15)
        # Look for version string like "5.38.0" in constant pool
        ver_match = re.search(r'(\d+\.\d+\.\d+\.\d+)', lout)
        if ver_match:
            version = ver_match.group(1)
        else:
            ver_match = re.search(r'(\d+\.\d+\.\d+)', lout)
            if ver_match:
                version = ver_match.group(1)
    except Exception as _e:
        log.warning("_find_runner_class: failed to detect JEB version: %s", _e)

    return found, version


def cmd_gen_runner(ctx: CmdContext):
    """Auto-detect script runner class and generate JebScriptRunner.java."""
    args, config = ctx.args, ctx.config
    jeb_dir = config.get("jeb", {}).get("install_dir", "")
    log.debug("cmd_gen_runner: jeb_dir=%s", jeb_dir)
    if not os.path.isdir(jeb_dir):
        _log_err(f"JEB install directory not found: {jeb_dir}")
        return

    cls_name, jeb_ver = _find_runner_class(jeb_dir, config)
    if not cls_name:
        return

    runner_path = os.path.join(jeb_dir, "JebScriptRunner.java")
    if os.path.isfile(runner_path):
        with open(runner_path, "r", encoding="utf-8") as f:
            existing = f.read()
        if f"import com.pnfsoftware.jeb.{cls_name};" in existing:
            if not _opt(args, 'force', False):
                _log_ok(f"JebScriptRunner.java already uses {cls_name} - up to date")
                _log_info("Use --force to regenerate")
                return

    source = _RUNNER_TEMPLATE.format(cls=cls_name, ver=jeb_ver)
    with open(runner_path, "w", encoding="utf-8") as f:
        f.write(source)
    _log_ok(f"Generated: {runner_path}")
    _log_info(f"  Runner class: com.pnfsoftware.jeb.{cls_name}")
    _log_info(f"  JEB version:  {jeb_ver}")

    project_runner = os.path.join(
        os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__)))),
        "jeb-runner", "JebScriptRunner.java")
    if os.path.isdir(os.path.dirname(project_runner)):
        with open(project_runner, "w", encoding="utf-8") as f:
            f.write(source)
        _log_ok(f"Updated project copy: {project_runner}")

    if _opt(args, 'no_compile', False):
        _log_info("Skipping compilation (--no-compile)")
        return

    _log_info("Compiling JebScriptRunner.java...")
    import subprocess as sp
    _, javac_bin, _, _ = _java_tools(config)
    cp = os.path.join(jeb_dir, "bin", "app", "*")

    try:
        sp.check_call(
            [javac_bin, "-cp", cp, "JebScriptRunner.java"],
            cwd=jeb_dir, timeout=60,
            stdout=sp.DEVNULL, stderr=sp.PIPE)
        class_path = os.path.join(jeb_dir, "JebScriptRunner.class")
        if os.path.isfile(class_path):
            _log_ok(f"Compiled: {class_path}")
            # Copy .class to project
            project_class = os.path.join(os.path.dirname(project_runner), "JebScriptRunner.class")
            if os.path.isdir(os.path.dirname(project_class)):
                import shutil
                shutil.copy2(class_path, project_class)
        else:
            _log_warn("javac succeeded but .class file not found")
    except sp.CalledProcessError as e:
        _log_err(f"Compilation failed: {e.stderr.decode() if e.stderr else 'unknown error'}")
        _log_info("Fix errors and compile manually:")
        _log_info(f'  cd {jeb_dir} && javac -cp "bin/app/*" JebScriptRunner.java')
    except FileNotFoundError:
        _log_err(f"javac not found: {javac_bin}")
        _log_info("Set jeb.java_home in config.json or add Java to PATH")


